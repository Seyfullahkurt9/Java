public class Kisi {

    private String isim;
    private int yas;

    public Kisi(String isim, int yas) {

        this.isim = isim;
        this.yas = yas;
    }

    public String getIsim() {

        return this.isim;
    }

    public int getYas() {

        return this.yas;
    }

    public void setIsim(String isim) {
        
        this.isim = isim;
    }
}